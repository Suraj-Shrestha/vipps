<?php

namespace zaporylie\Vipps\Tests\Unit\Resource\Payment;

use GuzzleHttp\Psr7\Response;
use function GuzzleHttp\Psr7\stream_for;
use zaporylie\Vipps\Model\Payment\RequestCancelPayment;
use zaporylie\Vipps\Model\Payment\ResponseCancelPayment;
use zaporylie\Vipps\Resource\Payment\CancelPayment;
use zaporylie\Vipps\Resource\HttpMethod;

class CancelPaymentTest extends PaymentResourceBaseTestBase
{

    /**
     * @var \zaporylie\Vipps\Resource\Payment\CancelPayment
     */
    protected $resource;

    /**
     * {@inheritdoc}
     */
    protected function setUp()
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->resource = $this->getMockBuilder(CancelPayment::class)
            ->setConstructorArgs([$this->vipps, 'test_subscription_key', 'test_order_id', new RequestCancelPayment()])
            ->disallowMockingUnknownTypes()
            ->setMethods(['makeCall'])
            ->getMock();

        $this->resource
            ->expects($this->any())
            ->method('makeCall')
            ->will($this->returnValue(new Response(200, [], stream_for(json_encode([])))));
    }

    /**
     * @covers \zaporylie\Vipps\Resource\Payment\CancelPayment::getBody()
     * @covers \zaporylie\Vipps\Resource\Payment\CancelPayment::__construct()
     */
    public function testBody()
    {
        $this->assertNotEmpty($this->resource->getBody());
        // Valid JSON.
        $this->assertNotNull(json_decode($this->resource->getBody()));
    }

    /**
     * @covers \zaporylie\Vipps\Resource\Payment\CancelPayment::getMethod()
     */
    public function testMethod()
    {
        $this->assertEquals(HttpMethod::PUT, $this->resource->getMethod());
    }

    /**
     * @covers \zaporylie\Vipps\Resource\Payment\CancelPayment::getPath()
     */
    public function testPath()
    {
        $this->assertEquals('/Ecomm/v1/payments/test_order_id/cancel', $this->resource->getPath());
    }

    /**
     * @covers \zaporylie\Vipps\Resource\Payment\CancelPayment::call()
     */
    public function testCall()
    {
        $this->assertInstanceOf(ResponseCancelPayment::class, $response = $this->resource->call());
        $this->assertEquals(new ResponseCancelPayment(), $response);
    }
}
